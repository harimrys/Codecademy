public class Continentes{

  public static void main(String[] args){

    int continente = 4;
    
    switch (continente) {
    case 1:
    System.out.println("Norte America, Mexico");
    break;

    case 2:
    System.out.println("Sur America, Brasil");
    break;

    case 3:
    System.out.println("Europa, Rusia");
    break;

    case 4:
    System.out.println("Africa, Nigeria");
    break;

    case 5:
    System.out.println("Asia, China");
    break;

    case 6:
    System.out.println("Australia, Sydney");
    break;

    case 7:
    System.out.println("Antartida");
    break;

    default:
    System.out.println("No hay continente");
    }
  }
}
